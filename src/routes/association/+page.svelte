    <h2>History of Association</h2>

    <ul>
        <li>
            There was no association in the national level for ventilating the
            issues / difficulties, being faced by the Chief Accounts Officers
            [Group-A General Central Service (Ministerial)] in various
            formations. Many of them are facing acute hardship towards end of
            their service career, being posted away from their home zone, on
            promotion. Particularly the lady officers had expressed their agony
            over the fact and urged that they should be accommodated in the home
            zone, even by temporary diversion of vacant posts of other zone.
        </li>
        <li>
            It is also felt that in absence of proper duty list, Chief Accounts
            Officers (CAOs) at different zones are tasked with the functions of
            Drawing and Disbursing Officer (D.D.O.) or even Administrative
            Officers which are meant for Group-B posts. They are denied of their
            legitimate space in the administration, despite the fact that Chief
            Accounts Officers plays an integral role in the financial operation
            of the Chief Commissioner Offices and Commissionerate Offices in the
            both formation of CGST & Customs.
        </li>
        <li>
            At the policy level, it was unequivocally felt that Chief Accounts
            Officers should get further financial up-gradation to Level-11
            (pre-revised Grade Pay Rs.6600 in PB-3). It is also appreciated by
            the CAOs present in the meeting that more than 50% of the Chief
            Accounts Officers would serve the post for a period of more than at
            least 4 years. It is rankingly painful for them to continue in the
            same post whereas their juniors in the other cadre are getting the
            career progression.
        </li>
        <li>
            After a threadbare discussion in several meetings, it was felt that
            an organization be formed at the national level to ventilate the
            issues of Chief Accounts Officer to the appropriate levels of
            administration. It was expressed by one and the all that because of
            absence of any legitimate platform, we are not able to properly
            address our issues and thereby is missing out on various fronts. We
            also not promote our common service interest. The need for a service
            association was thus felt necessary.
        </li>
        <li>
            Ultimately, it was unanimously decided to form an association namely
            “All India Customs, CGST and Directorates Chief Accounts Officers’
            Association” in the historically meeting (hybrid mode) of 9th April,
